fn has_project_anchor_ancestor(
    start: &Path,
    predicate: fn(&Path) -> bool,
    max_parent_hops: usize,
) -> bool {
    let mut current = Some(start);
    for _ in 0..=max_parent_hops {
        let Some(dir) = current else {
            return false;
        };
        if predicate(dir) {
            return true;
        }
        current = dir.parent();
    }

    false
}

fn has_dotnet_project_file(dir: &Path) -> bool {
    let Ok(entries) = fs::read_dir(dir) else {
        return false;
    };

    entries.flatten().any(|entry| {
        let path = entry.path();
        let Ok(metadata) = fs::symlink_metadata(&path) else {
            return false;
        };
        if !metadata.is_file() || crate::safety::is_reparse_like(&metadata) {
            return false;
        }

        path.extension()
            .and_then(|extension| extension.to_str())
            .is_some_and(|extension| {
                ["csproj", "fsproj", "vbproj"]
                    .iter()
                    .any(|expected| extension.eq_ignore_ascii_case(expected))
            })
    })
}

fn has_file_with_extension(dir: &Path, extensions: &[&str]) -> bool {
    let Ok(entries) = fs::read_dir(dir) else {
        return false;
    };

    entries.flatten().any(|entry| {
        let path = entry.path();
        let Ok(metadata) = fs::symlink_metadata(&path) else {
            return false;
        };
        if !metadata.is_file() || crate::safety::is_reparse_like(&metadata) {
            return false;
        }

        path.extension()
            .and_then(|extension| extension.to_str())
            .is_some_and(|extension| {
                extensions
                    .iter()
                    .any(|expected| extension.eq_ignore_ascii_case(expected))
            })
    })
}

fn is_composer_vendor_dir(path: &Path) -> bool {
    let Some(parent) = path.parent() else {
        return false;
    };

    has_regular_file(&parent.join("composer.json"))
}

fn has_child_dir(parent: &Path, name: &str) -> bool {
    let path = parent.join(name);
    let Ok(metadata) = fs::symlink_metadata(path) else {
        return false;
    };

    metadata.is_dir() && !crate::safety::is_reparse_like(&metadata)
}

fn has_regular_file(path: &Path) -> bool {
    let Ok(metadata) = fs::symlink_metadata(path) else {
        return false;
    };

    metadata.is_file() && !crate::safety::is_reparse_like(&metadata)
}
