#[derive(Debug, Clone, Copy)]
struct ProjectArtifactRule {
    definition: ProjectArtifactDefinition,
    context: ProjectArtifactContext,
}

#[derive(Debug, Clone, Copy)]
enum ProjectArtifactContext {
    NodeProject,
    TargetProject,
    PythonProject,
    PythonCache,
    GradleProject,
    DartProject,
    ZigProject,
    GenericProjectOutput,
    CocoapodsProject,
    CxxProject,
    ExpoProject,
    SwiftPackage,
    DotnetBin,
    DotnetObj,
    ComposerVendor,
}

const fn artifact_rule(
    directory_name: &'static str,
    rule_id: &'static str,
    restore_hint: &'static str,
    context: ProjectArtifactContext,
) -> ProjectArtifactRule {
    ProjectArtifactRule {
        definition: ProjectArtifactDefinition {
            directory_name,
            rule_id,
            restore_hint,
        },
        context,
    }
}

